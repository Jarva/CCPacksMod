package io.github.ThatRobin.ccpacks.Util;

public class ColourHolder {

    private float red;
    private float green;
    private float blue;


    public ColourHolder(float red, float green, float blue) {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }

    public float getBlue() {
        return this.blue;
    }

    public float getGreen() {
        return this.green;
    }

    public float getRed() {
        return this.red;
    }
}
